  <h1>Sampah Anorganik</h1>
  <p>Sampah anorganik adalah jenis sampah yang tidak dapat terurai secara alami dan berasal dari material yang tidak pernah hidup, seperti plastik, kaca, logam, dan lain-lain. Sampah anorganik memerlukan pengolahan khusus dan dapat mencemari lingkungan jika tidak dikelola dengan benar.</p>
  <h2>Contoh Sampah Anorganik</h2>
  <ul>
    <li>Plastik</li>
    <li>Kaca</li>
    <li>Logam</li>
    <li>Kain sintetis</li>
    <li>Kertas dan kardus yang terkontaminasi bahan kimia atau bahan berbahaya lainnya</li>
  </ul>
  <h2>Cara Mengelola Sampah Anorganik</h2>
  <ul>
    <li>Pisahkan sampah anorganik dari sampah organik</li>
    <li>Gunakan wadah yang terpisah untuk menyimpan sampah anorganik</li>
    <li>Buang sampah anorganik ke tempat pengolahan sampah anorganik yang telah ditentukan</li>
    <li>Gunakan produk yang ramah lingkungan untuk mengurangi jumlah sampah anorganik yang dihasilkan</li>
    <li>Daur ulang atau daur ulang kembali sampah anorganik yang masih dapat dimanfaatkan</li>
  </ul>
